/* USER CODE BEGIN 4 */
void HAL_GPIO_EXTI_Callback(uint16_t pin) {
	if (pin == GPIO_PIN_6) {
		if (pox->Interrupt == MAX326_IT_INIT) {
			USART_PRINT("\r\nInit completed");
			MAX326_ConfigBpm(pox, MAX326_MODE_ONE);
		}
		else if (pox->Interrupt == MAX326_IT_CONFBPM) {
			USART_PRINT("\r\nConfigBPM completed");
		}
	}
}

void HAL_I2C_MasterTxCpltCallback(I2C_HandleTypeDef *hi2c) {
	if (hi2c == &hi2c1) {
		MAX326_I2CTxCallback(pox);
	}
}

void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c) {
	if (hi2c == &hi2c1) {
		USART_PRINT("\r\nI2C received something");
		MAX326_I2CRxCallback(pox);
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim) {
	if (htim == &htim10) {
		MAX326_TimerCallback(pox);
	}
}
